package gui;

public class PromemoriaGUI {
	private Boolean impostare;
	private Integer days;
	private Integer hour_of_day;
	
	public Boolean getImpostare() {
		return impostare;
	}
	public void setImpostare(Boolean impostare) {
		this.impostare = impostare;
	}
	public Integer getDays() {
		return days;
	}
	public void setDays(Integer days) {
		this.days = days;
	}
	public Integer getHour_of_day() {
		return hour_of_day;
	}
	public void setHour_of_day(Integer hour_of_day) {
		this.hour_of_day = hour_of_day;
	}

}
